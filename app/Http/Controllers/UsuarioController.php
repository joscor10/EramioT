    {
         Usuario::create([
             'nombre'=>$request["nombre"],
             'apellido'=>$request["apellido"],
             'usuario'=>$request['usuario'],
              'identificacion'=>$request['identificacion'],
             'email'=>$request['email'],
               'password'=>bcrypt($request['contrasena']),
              'tipo'=>1
        
        ]);
          Session::flash('message-user','Usuario Creado Correctamente');
          return redirect('/'); 
    }

